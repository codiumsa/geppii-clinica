Bodega.Cliente = DS.Model.extend({
  persona: DS.belongsTo('persona', {embedded: 'always'}),
  referencias: DS.hasMany('referencia', {async: true}),
  documentos: DS.hasMany('documento', {async: true}),
  ingresoFamiliares: DS.hasMany('ingresoFamiliar', {async: true}),
  numeroCliente: DS.attr('number'),
  activo: DS.attr('boolean'),
  calificacion: DS.belongsTo('calificacion'),
  antiguedad: DS.attr('number'),
  salarioMensual: DS.attr('number'),
  ramo: DS.attr('string'),
  otrosIngresos: DS.attr('number'),
  empleador: DS.attr('string'),
  domicilio: DS.attr('string'),
  cargo: DS.attr('string'),
  telefono: DS.attr('string'),
  jubilado: DS.attr('string'),
  institucion: DS.attr('string'),
  comerciante: DS.attr('string'),
  matriculaNro: DS.attr('string'),
  empleado: DS.attr('boolean'),
  profesion: DS.attr('string'),
  actividadEmpleador: DS.attr('string'),
  ciudad: DS.belongsTo('ciudad',{async:true}),
  barrioEmpleador: DS.attr('string'),
  direccionEmpleador: DS.attr('string'),
  fechaPagoSueldo: DS.attr('date'),
  conceptoOtrosIngresos: DS.attr('string'),
  ips: DS.attr('boolean'),
  fechaIngresoInformconf: DS.attr('date'),
  fechaEgresoInformconf: DS.attr('date'),
  infoCliente: DS.attr('string'),
  razonSocialCliente: DS.attr('string'),
  idPersona: DS.attr('number')
});
